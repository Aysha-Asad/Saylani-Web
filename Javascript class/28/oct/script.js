//new Date() Method as an object
let currentDate = new Date();
console.log(currentDate.getDay()); //Output 0-6 (0=Sunday, 1=Monday, ...6=Saturday)
console.log(currentDate.getMonth()); //Output 0-11 (0=January, 1=February, ...11=December)
console.log(currentDate.getDate()) //Output 1-31 (Day of the month)
console.log(currentDate.getFullYear()); //Output 4 digit year (e.g., 2025)
console.log(currentDate.getHours()); //Output 0-23 0 = Midnight, 12 = Noon, 23 = 11 PM
console.log(currentDate.getMinutes()); //Output 0-59
console.log(currentDate.getSeconds()); //Output 0-59
console.log(currentDate.getMilliseconds()); //Output 0-999
console.log(currentDate.getTime()); //Output milliseconds since January 1, 1970
console.log(currentDate.getTimezoneOffset()); //Output difference in minutes between UTC and local time







